void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;

    long long ans = n;

    for (int i = n - 1; i > 0; --i)
        ans += (s[i] == s[i - 1] ? 0 : i);

    std::cout << ans << "\n";
}

